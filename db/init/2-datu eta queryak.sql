from grupos
join participantes on grupos.id_grupo=participantes.id_grupo
join usuarios on usuarios.id_usuario=participantes.id_usuario
where usuarios.id_usuario=3;


//PARTICIPANTES DE CADA GRUPO Y NOMBRE DEL GRUPO;
